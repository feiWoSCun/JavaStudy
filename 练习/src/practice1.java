//java语言程序的注释，注释不会被编译//
public class practice1 {
    public static void main(String[] args){//公开静态主程序//
        System.out.println("giegie，你男朋友知道了不会揍我吧");//向控制台输出一段消息,java中所有的字符串都要""括起来//
    }
}
class practice2{
    public static void main(String[] args){
        System.out.println("hello world");
    }
}
