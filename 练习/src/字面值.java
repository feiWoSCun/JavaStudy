public class 字面值 {
    public static void main(String[] args){
        System.out.println("giegie,你女朋友知道了不会揍我吧");
        System.out.println('a');
        System.out.println(3.1415926);
    }
}
/**
 * 字面值(在c语言中叫做常量)1 100 1000，3.14，abc，true，false 这些都是字面值
 * 字面值是Java源程序组成程序之一，包括标识符和关键字
 * 10，100这些属于整数型字面值
 * 3.14属于浮点型字面值
 * true false 属于布尔型字面值
 * ”abc“”中国人“属于字符串型字面值。注意：Java中所有字符串型字面值必须使用双引号（半角)括起来
 *所有的字符型字面值必须使用单引号括起来，单引号是半角
 *”a“”人“属于字符型字面值
 */